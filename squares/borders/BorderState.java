/**
 * The state of a border.
 * 
 * @author 	Stef Noten & Jasper Hilven
 * @version	1.0
 */
public enum BorderState {
	/**
	 * The uninitialized state.
	 */
	UNINITIALISED,
	/**
	 * The initialized state.
	 */
	INITIALISED,
	/**
	 * The terminated state.
	 */
	TERMINATED
}
